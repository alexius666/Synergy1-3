class Main {
    public static void main(String[] args) {
        int width = 16; // ширина прямоугольника
        int height = 21; // высота прямоугольника
        int square = width * height; // вычисление площади прямоугольника
        int perimeter = 2 * width + 2 * height; // // вычисление перимерта прямоугольника

        System.out.println("Площадь прямоугольника равна " + square);
        System.out.println("Периметр прямоугольника равен " + perimeter);
    }
  
}  
